TRANSFORMER_TO_OSGB36 = pyproj.Transformer.from_crs(
    "EPSG:4326", "EPSG:27700", always_xy=True
)


def pct(value: float) -> str:
    return f"{value * 100:.1f}%"


def display_borough_name(name: str) -> str:
    return name.replace(" Upon ", " upon ").replace(" And ", " and ")


def normalise_borough_name(name: str) -> str:
    return re.sub(r"[^a-z0-9]", "", name.lower().replace("&", "and"))


def save_figure(path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    plt.savefig(path, dpi=220, bbox_inches="tight")
    plt.close()


def memo_path(stem: str) -> Path:
    return DATA_DIR / f"report_figure_{stem}_memo.md"


def write_figure_memo(
    stem: str,
    title: str,
    figure_path: Path,
    source: str,
    filter_text: str,
    denominator: str,
    suggested_caption: str,
    notes: Iterable[str],
) -> None:
    lines = [
        f"# {title}",
        "",
        "> Auto-generated by `src/data/build_report_evaluation_figures.py`.",
        "",
        f"- **Figure**: `{figure_path.relative_to(PROJECT_ROOT)}`",
        f"- **Source artefact**: `{source}`",
        f"- **Filter**: {filter_text}",
        f"- **Denominator**: {denominator}",
        f"- **Generation command**: `python src/data/build_report_evaluation_figures.py`",
        "",
        "## Suggested Caption",
        "",
        suggested_caption,
        "",
        "## Notes",
        "",
    ]
    lines.extend([f"- {note}" for note in notes])
    memo_path(stem).write_text("\n".join(lines) + "\n")


def load_canonical() -> pd.DataFrame:
    df = pd.read_parquet(CANONICAL_PATH)
    df = df[df["IncidentGroup"].isin(INCIDENT_GROUPS)].copy()
    df["exceeds_six_min_target"] = df["exceeds_six_min_target"].astype("boolean")
    return df


def borough_exceedance_table(df: pd.DataFrame) -> pd.DataFrame:
    recorded = df[df["response_time_seconds"].notna()].copy()
    return (
        recorded.groupby("borough_canonical")
        .agg(
            incident_count=("IncidentNumber", "size"),
            exceeds_six_min_share=("exceeds_six_min_target", "mean"),
            median_response_min=("response_time_minutes", "median"),
            precise_coord_share=("coord_precise_valid", "mean"),
        )
        .sort_values("exceeds_six_min_share", ascending=False)
    )


def plot_motivation_borough_exceedance_map(df: pd.DataFrame) -> dict[str, object]:
    recorded = df[df["response_time_seconds"].notna()].copy()
    ranking = borough_exceedance_table(df)
    ranking_by_key = {
        normalise_borough_name(name): row for name, row in ranking.iterrows()
    }
    overall = float(recorded["exceeds_six_min_target"].mean())
    top = ranking.head(8).iloc[::-1]
    geojson = json.loads(BOUNDARY_PATH.read_text())
    cmap = colormaps["YlOrRd"]
    norm = Normalize(
        vmin=float(ranking["exceeds_six_min_share"].min()),
        vmax=float(ranking["exceeds_six_min_share"].max()),
    )

    fig, (map_ax, bar_ax) = plt.subplots(
        1, 2, figsize=(14.5, 7.8), gridspec_kw={"width_ratios": [1.2, 1]}
    )
    missing_boundaries: list[str] = []
    east_bounds: list[float] = []
    north_bounds: list[float] = []

    for feature in geojson["features"]:
        borough_name = feature["properties"]["name"]
        row = ranking_by_key.get(normalise_borough_name(borough_name))
        if row is None:
            missing_boundaries.append(borough_name)
        fill = "#e5e7eb" if row is None else cmap(norm(row["exceeds_six_min_share"]))
        for ring in iter_boundary_rings(feature["geometry"]):
            lon = [coord[0] for coord in ring]
            lat = [coord[1] for coord in ring]
            east, north = TRANSFORMER_TO_OSGB36.transform(lon, lat)
            east_bounds.extend(east)
            north_bounds.extend(north)
            map_ax.add_patch(
                Polygon(
                    np.column_stack([east, north]),
                    closed=True,
                    facecolor=fill,
                    edgecolor="#111827",
                    linewidth=0.5,
                    alpha=0.92,
                )
            )
    map_ax.set_aspect("equal", adjustable="box")
    map_ax.set_axis_off()
    map_ax.set_xlim(min(east_bounds) - 4500, max(east_bounds) + 4500)
    map_ax.set_ylim(min(north_bounds) - 4500, max(north_bounds) + 4500)
    map_ax.set_title("London borough exceedance share", fontsize=13, pad=10)
    sm = ScalarMappable(cmap=cmap, norm=norm)
    sm.set_array([])
    cbar = fig.colorbar(sm, ax=map_ax, fraction=0.036, pad=0.02)
    cbar.set_label("Incidents exceeding six minutes")
    cbar.ax.yaxis.set_major_formatter(PercentFormatter(xmax=1.0, decimals=0))

    y = np.arange(len(top))
    colors = cmap(norm(top["exceeds_six_min_share"]))
    bar_ax.barh(y, top["exceeds_six_min_share"] * 100, color=colors)
    bar_ax.axvline(overall * 100, color="#264653", lw=1.5)
    bar_ax.set_yticks(y, [display_borough_name(name) for name in top.index])
    bar_ax.set_xlabel("Incidents exceeding six minutes (%)")
    bar_ax.set_title("Highest recorded-time boroughs", fontsize=13, pad=10)
    bar_ax.grid(axis="x", alpha=0.25)
    for idx, (_, row) in enumerate(top.iterrows()):
        bar_ax.text(
            row["exceeds_six_min_share"] * 100 + 0.6,
            idx,
            f"{row['exceeds_six_min_share'] * 100:.1f}%",
            va="center",
            fontsize=8,
        )
    bar_ax.text(
        overall * 100 + 0.6,
        -0.62,
        f"London reference {overall * 100:.1f}%",
        fontsize=8.5,
        color="#264653",
        va="center",
    )
    bar_ax.set_xlim(0, max(58, top["exceeds_six_min_share"].max() * 115))
    bar_ax.set_ylim(-0.9, len(top) - 0.25)
    fig.suptitle(
        "Motivating response-performance gap in the 2024-2026 LFB slice",
        fontsize=15,
        y=0.98,
    )
    fig.tight_layout(rect=[0, 0, 1, 0.95])
    save_figure(FIGURES["motivation_borough_exceedance_map"])

    top3 = [
        {
            "borough": display_borough_name(name),
            "exceedance_share": float(row["exceeds_six_min_share"]),
            "incident_count": int(row["incident_count"]),
        }
        for name, row in ranking.head(3).iterrows()
    ]
    write_figure_memo(
        "motivation_borough_exceedance_map",
        "Motivation Borough Exceedance Map Memo",
        FIGURES["motivation_borough_exceedance_map"],
        f"{CANONICAL_PATH.relative_to(PROJECT_ROOT)} + {BOUNDARY_PATH.relative_to(PROJECT_ROOT)}",
        "IncidentGroup in {False Alarm, Fire, Special Service}; response time recorded; joined to ONS/GLA borough boundaries by canonical borough name.",
        "Incidents with non-null `FirstPumpArriving_AttendanceTime`, grouped by borough.",
        "Borough choropleth and top-borough bar chart showing six-minute exceedance shares in the 2024--2026 LFB slice; the reference line marks the project-wide recorded-time exceedance share.",
        [
            f"Overall recorded-time exceedance share: {pct(overall)}.",
            f"Top three boroughs: {', '.join(item['borough'] + ' ' + pct(item['exceedance_share']) for item in top3)}.",
            f"Boundary features without a matching canonical borough row: {missing_boundaries or 'none'}.",
            "This is a descriptive motivation figure, not a causal or routing-grade explanation of the borough differences.",
        ],
    )
    return {
        "overall_recorded_time_exceedance_share": overall,
        "top_three_boroughs": top3,
        "boundary_join_missing": missing_boundaries,
    }


def add_box(
    ax: plt.Axes,
    xy: tuple[float, float],
    width: float,
    height: float,
    title: str,
    body: str,
    facecolor: str,
    edgecolor: str = "#111827",
    title_color: str = "#111827",
) -> None:
    rect = Rectangle(
        xy,
        width,
        height,
        linewidth=1.2,
        edgecolor=edgecolor,
        facecolor=facecolor,
        joinstyle="round",
    )
    ax.add_patch(rect)
    title_offset = min(0.055, height * 0.22)
    body_gap = min(0.095, height * 0.34)
    body_font = 8.1 if height < 0.16 else 9.0
    wrapped_body = "\n".join(
        textwrap.fill(line, width=max(22, int(width * 80))) for line in body.splitlines()
    )
    ax.text(
        xy[0] + 0.03,
        xy[1] + height - title_offset,
        title,
        fontsize=10.8,
        weight="bold",
        color=title_color,
        va="top",
    )
    ax.text(
        xy[0] + 0.03,
        xy[1] + height - title_offset - body_gap,
        wrapped_body,
        fontsize=body_font,
        color="#111827",
        va="top",
        linespacing=1.25,
    )


def add_arrow(
    ax: plt.Axes,
    start: tuple[float, float],
    end: tuple[float, float],
    color: str = "#374151",
) -> None:
    ax.add_patch(
        FancyArrowPatch(
            start,
            end,
            arrowstyle="-|>",
            mutation_scale=14,
            linewidth=1.2,
            color=color,
            shrinkA=4,
            shrinkB=4,
        )
    )


def add_compact_box(
    ax: plt.Axes,
    xy: tuple[float, float],
    width: float,
    height: float,
    title: str,
    body: str,
    facecolor: str,
    edgecolor: str,
) -> None:
    ax.add_patch(
        Rectangle(
            xy,
            width,
            height,
            linewidth=1.35,
            edgecolor=edgecolor,
            facecolor=facecolor,
        )
    )
    ax.text(
        xy[0] + width / 2,
        xy[1] + height * 0.64,
        title,
        fontsize=12.5,
        weight="bold",
        ha="center",
        va="center",
        color="#111827",
    )
    ax.text(
        xy[0] + width / 2,
        xy[1] + height * 0.34,
        body,
        fontsize=9.8,
        ha="center",
        va="center",
        color="#374151",
        linespacing=1.22,
    )


