def plot_temporal_density(df: pd.DataFrame) -> dict[str, object]:
    fig, axes = plt.subplots(1, 3, figsize=(15, 5.8), sharey=True)
    peak_summary: dict[str, dict[str, object]] = {}
    max_count = 0
    matrices: dict[str, pd.DataFrame] = {}
    for group in INCIDENT_GROUPS:
        matrix = (
            df[df["IncidentGroup"] == group]
            .groupby(["weekday_name", "hour_of_day"])
            .size()
            .unstack(fill_value=0)
            .reindex(index=WEEKDAY_ORDER, columns=range(24), fill_value=0)
        )
        matrices[group] = matrix
        max_count = max(max_count, int(matrix.to_numpy().max()))

    for ax, group in zip(axes, INCIDENT_GROUPS, strict=True):
        matrix = matrices[group]
        image = ax.imshow(matrix, aspect="auto", cmap="magma", vmin=0, vmax=max_count)
        ax.set_title(group)
        ax.set_xlabel("Hour of day")
        ax.set_xticks([0, 6, 12, 18, 23])
        if ax is axes[0]:
            ax.set_yticks(range(len(WEEKDAY_ORDER)), WEEKDAY_ORDER)
        else:
            ax.tick_params(axis="y", left=False)
        peak = matrix.stack().sort_values(ascending=False).index[0]
        peak_value = int(matrix.loc[peak[0], peak[1]])
        peak_summary[group] = {
            "weekday": peak[0],
            "hour_of_day": int(peak[1]),
            "incident_count": peak_value,
        }
        ax.scatter([peak[1]], [WEEKDAY_ORDER.index(peak[0])], s=55, facecolors="none", edgecolors="white", lw=1.6)
    fig.colorbar(image, ax=axes, shrink=0.78, label="Incident count")
    fig.suptitle("Hour-of-day by weekday incident density")
    save_figure(FIGURES["temporal_incident_density"])

    write_figure_memo(
        "temporal_incident_density",
        "Temporal Incident Density Memo",
        FIGURES["temporal_incident_density"],
        str(CANONICAL_PATH.relative_to(PROJECT_ROOT)),
        "IncidentGroup in {False Alarm, Fire, Special Service}.",
        "All canonical incident rows in the three main incident groups, counted by weekday and hour.",
        "Small-multiple heatmaps of incident counts by weekday and hour of day for each incident group; the white ring marks the peak cell in each panel.",
        [
            f"Peak cells: {json.dumps(peak_summary, sort_keys=True)}.",
            "Counts are descriptive demand-density evidence; they are not response-time or routing estimates.",
        ],
    )
    return {"temporal_peak_cells": peak_summary}


def plot_false_alarm_sensitivity(df: pd.DataFrame) -> dict[str, object]:
    recorded = df[df["response_time_seconds"].notna()].copy()

    def borough_share(frame: pd.DataFrame) -> pd.Series:
        return frame.groupby("borough_canonical")["exceeds_six_min_target"].mean()

    all_share = borough_share(recorded)
    no_false_share = borough_share(recorded[recorded["IncidentGroup"] != "False Alarm"])
    fire_share = borough_share(recorded[recorded["IncidentGroup"] == "Fire"])
    comp = pd.DataFrame(
        {
            "All incidents": all_share,
            "Excluding false alarms": no_false_share,
            "Fire only": fire_share,
        }
    ).dropna()
    top_boroughs = comp.sort_values("All incidents", ascending=False).head(12).index
    plot = comp.loc[top_boroughs].iloc[::-1]

    fig, ax = plt.subplots(figsize=(10.8, 7.4))
    y = np.arange(len(plot))
    markers = ["o", "s", "^"]
    colors = ["#264653", "#2a9d8f", "#e76f51"]
    for label, marker, color in zip(plot.columns, markers, colors, strict=True):
        ax.scatter(plot[label] * 100, y, label=label, marker=marker, s=80, color=color)
    for idx, (_, row) in enumerate(plot.iterrows()):
        ax.plot(row * 100, [idx] * 3, color="#9ca3af", lw=1, alpha=0.65)
    ax.set_yticks(y, plot.index)
    ax.set_xlabel("Incidents exceeding six minutes (%)")
    ax.set_title("False-alarm sensitivity of high-exceedance borough rankings")
    ax.grid(axis="x", alpha=0.25)
    ax.legend(frameon=False, loc="lower right")
    fig.tight_layout()
    save_figure(FIGURES["false_alarm_sensitivity"])

    rank = comp.rank(ascending=False, method="min")
    rank["shift_excluding_false_alarms"] = rank["Excluding false alarms"] - rank["All incidents"]
    rank["shift_fire_only"] = rank["Fire only"] - rank["All incidents"]
    max_shift_no_false = rank["shift_excluding_false_alarms"].abs().sort_values(ascending=False)
    spearman_no_false = comp["All incidents"].corr(
        comp["Excluding false alarms"], method="spearman"
    )
    spearman_fire = comp["All incidents"].corr(comp["Fire only"], method="spearman")
    write_figure_memo(
        "false_alarm_sensitivity",
        "False Alarm Sensitivity Memo",
        FIGURES["false_alarm_sensitivity"],
        str(CANONICAL_PATH.relative_to(PROJECT_ROOT)),
        "IncidentGroup in {False Alarm, Fire, Special Service}; response time recorded.",
        "Borough-level six-minute exceedance shares under three denominators: all incidents, excluding false alarms, and fire only.",
        "Sensitivity check comparing high-exceedance boroughs under all incidents, excluding false alarms, and fire-only denominators.",
        [
            f"Spearman rank correlation, all vs excluding false alarms: {spearman_no_false:.3f}.",
            f"Spearman rank correlation, all vs fire only: {spearman_fire:.3f}.",
            f"Largest rank shift when excluding false alarms: {max_shift_no_false.index[0]} ({max_shift_no_false.iloc[0]:.0f} places).",
        ],
    )
    return {
        "spearman_all_vs_excluding_false_alarms": float(spearman_no_false),
        "spearman_all_vs_fire_only": float(spearman_fire),
        "largest_rank_shift_excluding_false_alarms_borough": max_shift_no_false.index[0],
        "largest_rank_shift_excluding_false_alarms_places": float(max_shift_no_false.iloc[0]),
    }


def iter_boundary_rings(geometry: dict[str, object]) -> Iterable[list[list[float]]]:
    if geometry["type"] == "Polygon":
        for ring in geometry["coordinates"]:  # type: ignore[index]
            yield ring
    elif geometry["type"] == "MultiPolygon":
        for polygon in geometry["coordinates"]:  # type: ignore[index]
            for ring in polygon:
                yield ring


def plot_boundary(
    ax: plt.Axes,
    color: str = "#111827",
    linewidth: float = 0.45,
    alpha: float = 0.55,
) -> None:
    geojson = json.loads(BOUNDARY_PATH.read_text())
    for feature in geojson["features"]:
        for ring in iter_boundary_rings(feature["geometry"]):
            lon = [coord[0] for coord in ring]
            lat = [coord[1] for coord in ring]
            east, north = TRANSFORMER_TO_OSGB36.transform(lon, lat)
            ax.plot(east, north, color=color, lw=linewidth, alpha=alpha)


