def run_validation_checks(summary: dict[str, object]) -> None:
    for key, path in FIGURES.items():
        if not path.exists() or path.stat().st_size < 10_000:
            raise RuntimeError(f"{key} figure missing or unexpectedly small: {path}")
        mp = memo_path(key)
        if not mp.exists() or "Suggested Caption" not in mp.read_text():
            raise RuntimeError(f"{key} memo missing suggested caption: {mp}")
    precise_share = float(summary["coordinate_coverage"]["overall_precise_coordinate_share"])
    if not 0.35 <= precise_share <= 0.41:
        raise RuntimeError(f"precise coordinate share out of expected band: {precise_share}")
    if summary["borough_ranking"]["top_borough"] != "Hillingdon":
        raise RuntimeError("unexpected top borough in ranking; check data drift")
    if len(summary["temporal_density"]["temporal_peak_cells"]) != 3:
        raise RuntimeError("temporal heatmap did not record all incident groups")


